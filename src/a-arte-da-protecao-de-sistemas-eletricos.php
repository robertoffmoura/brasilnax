													<div class="paragraph" style="text-align:justify;"><strong>Fale um pouquinho da sua vida profissional </strong><br>Depois de voltar para o Brasil e terminar o curso de Energia e Automação na USP, ingressei em um programa de trainees da Companhia de Transmissão de Energia Elétrica Paulista - CTEEP, onde estou há quase 4 anos.<br><br>Meu trabalho é na área de estudos de proteção, que é um ramo da automação dos sistemas elétricos. É uma área que exige bastante domínio de aspectos teóricos, e apresenta desafios intelectuais para se chegar a um desempenho adequado de um sistema que é tão vital (um erro pode significar a origem de um blackout). Há quem diga que proteção de sistemas elétricos é uma arte tanto quanto uma técnica. O ramo de sistemas de potência sempre foi muito conservador, mas hoje estamos no início de uma nova era de inovações, que têm sido coletivamente chamadas de "smart grid". Pouco a pouco elas irão transformar esta indústria para resolver novos desafios, como integração de energias intermitentes, geração solar nos telhados, carros elétricos, termostatos e lava-louças inteligentes, e outros.<br><br>Na minha avaliação, a área de energias e sistemas de potência era até pouco tempo dominada por engenheiros formados ainda na década de 80; não atraia novos profissionais e nem novas tecnologias. Isso está mudando, tanto porque uma geração inteira está se aposentando, criando demanda por novos profissionais, quanto porque novos desafios e novas tecnologias para atacá-los estão convergindo para criar um ambiente de inovação, atrativo para engenheiros interessados no tema de energias, que voltou a estar em voga. Essa é uma tendência mundial. Aos que desejarem voltar ao Brasil, encontrarão um país que acaba de passar por (mais uma) crise energética e que portanto, pelo menos em teoria, está preparado para valorizar os profissionais da área de energias.<br><br><strong>Vida na X</strong><br>Na X, depois do programa básico carregado de matemática e física, segui o programa de Energias do Século 21. De maneira geral, gostei muito das matérias, e aprendi coisas que eu não teria aprendido na Poli. Alguns outros pontos positivos foram as várias viagens pela Europa, estágio de verão na Itália, a banda de rock que tínhamos na X (banda deGrau), o incentivo ao esporte e, claro, Paris.</div>
