														<strong>Como são os professores em geral? São especialistas com renome na área? </strong><br>Três professores são responsáveis pelo PA de MAP ( C. Graham, E. Le Pennec, O. Pantz), mas como o PA possui várias subdivisões, cada temática possui um ou dois outros professores responsáveis.<br><br>Os professores do departamento, em geral, são especialistas no domínio. Particularmente, no meu percurso (Probabilidade e Finança), professores como N. Touzi (Cálculo estocástico) e E. Gobet (Monte-Carlo e processos estocásticos) são especialistas de renome na área. Acredito que essa seja uma realidade média na X.<br>
