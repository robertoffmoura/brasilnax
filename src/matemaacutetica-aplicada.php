													<div class="paragraph"><span>Isso torna o PA MAP bastante rico, pois é talvez um dos mais aplicados ao estudo de problemas da vida real, sem, no entanto, perder o rigor teórico. A formação foi pensada para ser adaptada à aqueles que querem trabalhar no mundo das empresas, mas igualmente para aqueles que tem um perfil mais de pesquisa.</span></div>
													<div class="paragraph" style="text-align:justify;">
														<strong>Quais os tipos de matérias que se pode fazer? </strong><br>O PA não possui apenas matérias de matemática aplicada. De fato, pra cada uma das subdivisões, os alunos podem até fazer cursos de economia, informática, mecânica, matemática ou física. Além do que a Polytechnique sempre permite leves alterações na construção do PA.<br><br>Em alguns percursos, como MAP-MEC ou MAP-INF, o estudante pode até fazer metade dos cursos de MAP e metade de uma segunda área.<br><br>De acordo com o departamento de matemática aplicada, as possíveis orientações de um PA MAP são:
														<ul>
