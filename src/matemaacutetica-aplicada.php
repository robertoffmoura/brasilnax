e pode trabalhar depois de terminar o PA? </strong><br><span>Você pode saber mais sobre isso conferindo o </span><a target="_blank" href="http://www.cmap.polytechnique.fr/~giraud/MetiersMaths.html">site</a><span> do departamento.</span></div>
