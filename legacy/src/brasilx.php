													<div class="paragraph" style="text-align:justify;">O Brasil'X (antigo Binet Latino) é a associação dos estudantes brasileiros da École Polytechnique. Criado pelas primeiras promoções de alunos brasileiros na X, o Brasil'X tem como objetivos promover a cultura brasileira na terra do <em>fromage</em>, divulgar a nossa querida escola do outro lado do Atlântico, apoiar o ensino da Língua Portuguesa na escola e integrar os calouros <em>polytechniciens</em> brasileiros que chegam ao início de cada ano.<br><br>A gestão atual do Brasil'X é composta por alunos da promoção 2017, que trabalham arduamente para honrar a história e os compromissos de um dos Binets mais queridos do Platâl. Entre as atividades desenvolvidas pelo Binet nesse ano, podemos citar:<br><br>- <strong>Churrasco de recepção</strong>: O Churrasco de Recepção brasileiro, cuja tradição remonta aos tempos de Cauchy, foi organizado por uma comissão da gestão do Brasil'X. O objetivo do churrasco é integrar os recém-chegados e criar laços entre a comunidade <em>polytechnicienne</em> brasileira. Todas as promoções são convidadas ao evento e sempre temos a presença de diversos X de promoções bem antigas (em 2016, por exemplo, contamos com a presença de X2005, X2008, e por aí vai...).<br><br>- <strong>Monitoria de Língua Portuguesa</strong>: Três membros do Brasil'X foram responsáveis em 2016 pela Monitoria de Língua Portuguesa. Cada tutor acompanha dois alunos do curso de Português e contribui para o seu progresso na língua através da realização de almoços-conversação. São uma ou duas horinhas semanais de um bom bate-papo em Português na hora do almoço!<br><br>- <strong>Copa do Mundo de Futsal</strong>: <span style="color:rgb(153, 153, 153)">Apesar das incontáveis nacionalidades presentes na X, nunca nenhum Binet realizou uma competição esportiva entre países. O Brasil'X inovou mais uma vez e tirou o projeto do papel. A gestão do Brasil'X de 2016 teve a ideia de fazer uma competição de futsal no formato Copa do Mundo com 12 seleções e duração de 2 dias, que agora acontece todo ano no campus. O campeão de 2016 levou um troféu e ingressos para um jogo do PSG no Parc des Princes!</span><br><br>- <strong>Soirée Forró</strong>: Todos os anos, a X organiza a Semana das Línguas e Culturas, onde vários Binets internacionais desenvolvem atividades que promovem suas respectivas culturas. Ao lembrarmos que existem mais de 60 nacionalidades na École Polytechnique, fica fácil perceber que essa é uma semana muito especial e muito rica. Em 2016, o Brasil'X teve o prazer de encerrar a Semana das Línguas e Culturas com a Soirée Forró. Chamamos um grupo musical e uma trupe de dançarinos de Forró para fazermos uma Iniciação ao Forró no Bôbar e preparamos coxinhas, feijoada, palha italiana e caipirinha para tornar a noite ainda mais brasileira! Resultado: uma noite regada a muito Forró e àquele tempero especial que todos conhecemos bem!</div>
