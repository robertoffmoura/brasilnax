													<div class="paragraph" style="text-align:justify;">
														<strong>Fale um pouquinho da sua vida profissional </strong><br>Atualmente, sou doutorando em teoria do controle no centro de matemática aplicada da X, depois de ter vindo para cá na promo X2008, voltado para o Brasil para terminar meu duplo diploma em Engenharia Elétrica na EESC - USP São Carlos, e voltado para a França para fazer um mestrado M2 Análise Numérica e EDPs, também na X. Sempre me interessei pela área acadêmica e, desde o começo do meu curso no Brasil, meus planos sempre foram de fazer um mestrado e doutorado em matemática para permanecer na pesquisa. Apesar de a X focar bastante no mercado de trabalho e na criação de start-ups - aparentemente hoje em dia mais ainda do que quando entrei -, o curso da X também é excelente para quem pretende fazer pesquisa. Eu já gostava bastante da teoria do controle antes mesmo de entrar na X, e decidi partir para a pesquisa nessa área depois de ter feito um <a href="./estaacutegio-de-pesquisa.html">estágio de pesquisa </a>com o professor do curso de controle de 3º ano na X, meu atual orientador. O ambiente de pesquisa na X é muito bom. Durante o meu doutorado, pude participar de várias conferências e conhecer vários pesquisadores da área, o que está sendo bem útil pra mim agora que estou procurando um pós-doutorado para depois que eu terminar meu doutorado em setembro. Sem dúvida, ter vindo pra cá ajudou bastante na minha perspectiva profissional na área acadêmica.<br><br><strong>Vida na X</strong><br>Por partes:
														<ul>
															<li><a href="./binets.html">Binets</a>: quando cheguei na X, eu não estava muito motivado pra participar de binets, e acabei não participando de nenhum. Talvez eu me arrependa um pouco hoje, a possibilidade de fazer outras atividades além dos estudos na X é bem grande, mas não era muito o que eu procurava quando entrei.</li>
															<li>Experiências: pra quem sempre tinha morado em cidade pequena no Brasil, morar perto de Paris foi uma experiência e tanto. Apesar dos 295 degraus da escada que leva até a estação de Lozère e o tempo de trem pra chegar a Paris, eu ia pra lá quase todo fim de semana pra fazer alguma coisa - apesar de eu nunca ter completado o meu plano de visitar o Louvre inteiro. A França tem uma coisa maravilhosa pra quando se é jovem: entradas de graça na maior parte dos museus e monumentos pra quem tem menos de 26 anos, além de descontos nos transportes e entradas bem mais baratas nos cinemas. Acho que é uma boa ir com frequência andar em Paris quando se mora na X, porque o campus aqui é bem isolado e faz bem sair um pouco do campus às vezes, principalmente aos finais de semana quando isso aqui fica bem mais vazio.</li>
															<li>Matérias: os cursos na X em geral são muito bons e de um nível excelente! Eles só me motivaram ainda mais nos meus planos de continuar na área acadêmica. Curti bastante a multidisciplinaridade do <a href="./segundo-ano.html">2º ano</a>, em que você tem que escolher cursos de 6 departamentos diferentes (ao menos era essa a regra na minha época¹). Fiz um <a href="./terceiro-ano.html">3º ano</a> em matemática, e, apesar de os cursos serem bem puxados (beeeem puxados), aprendi bastante coisa interessante.</li>
															<li><a href="./estaacutegios.html">Estágios</a>: o estágio de final do 3º ano da X foi certamente o que me permitiu continuar na área acadêmica. Fiz ele com o professor do curso de controle de 3º ano da X, que acabou sendo meu orientador de doutorado depois. Além de eu ter tido a possibilidade de trabalhar com pesquisa de verdade pela primeira vez, eu consegui os contatos que me permitiram voltar pra França depois de ter feito o duplo diploma no Brasil.</li>
															<li>Especialização: achei a oferta de programas de <a href="./terceiro-ano.html">3º ano </a>na X muito boa. Consegui fazer uma escolha de cursos bem adaptada ao que eu queria. Acho que a grande vantagem dos programas de 3º ano é que, além de ter vários, há vários percursos dentro de cada um deles, acaba sendo algo quase personalizado.</li>
															<li><a href="./esportes.html">Esporte</a>: não faço muito o tipo esportivo, mas não tive tantos problemas com o esporte na X. Escolhi natação, e acabei matando várias aulas no 3º ano e ficando com um belo de um C em esporte...</li>
															<li>Viagens: viajar dentro da Europa é bem fácil e relativamente barato, e o sistema de férias aqui (uma semana de férias no meio de fevereiro, uma em abril, férias longas em julho / agosto, uma semana em outubro, e duas em dezembro) facilita bastante pra se fazer umas viagens curtas. Acabei indo pra vários países durante o meu tempo aqui (sempre passei as férias de verão aqui, só voltava pro Brasil em dezembro, então em geral eu tinha umas 6 semanas pra viajar, e eu viajava esse tempo todo!), é uma ótima experiência. Também tem as viagens organizadas pela X, tipo a viagem de opção esportiva, que, no meu ano, me levou pra passar uma semana nas Ilhas Canárias. Realmente vale a pena!</li>
														</ul>
