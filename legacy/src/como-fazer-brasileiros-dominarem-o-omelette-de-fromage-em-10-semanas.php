													<div class="paragraph" style="text-align:justify;"><strong><font color="#2a2a2a">Percurso</font></strong><br>Durante sua dupla formação em Literatura e Linguística, Joëlle teve a oportunidade de conhecer professores que trabalhavam na análise do discurso e na didática do francês como língua estrangeira. Foi a paixão por este assunto combinada com o sono de infância de ser professora que a levaram a escolher esta profissão.<br><br>Trabalhava como professora de francês na <a target="_blank" href="http://www.epf.fr/fr">EPF</a>, uma escola francesa de engenharia, quando eles passaram a ser responsável pelo curso de francês durante o semestre preparatório na École Polytechnique. Além disso, ela trabalha como professora durante o <a href="./tronco-comum.html">Tronco Comum</a>.</div>
													<div class="paragraph" style="text-align:justify;"><strong><font color="#2a2a2a">O grupo desafio</font></strong><br>Durante o semestre preparatório, os alunos são divididos em quatro grupos dos quais o primeiro deles é formado por alunos latinos majoritariamente brasileiros que nunca tiveram um contato com a língua francesa. É esse desafio de ensinar uma nova língua do zero em apenas nove semanas que faz com que Joëlle sempre trabalhe com o “grupo dos brasileiros”. Para ela, outro aspecto interessante é que os alunos não têm maus hábitos de um ponto de vista linguístico, cabe a ela criar os bons hábitos.</div>
													<div class="paragraph" style="text-align:justify;"><strong><font color="#2a2a2a">Os brasileiros</font></strong><br>Para ela, o grupo dos brasileiros é um misto entre aulas divertidas e cansativas. Caracterizando os alunos como pessoas “fáceis no contato”, nos contou que eles sempre são muito participativos durante as aulas, mas às vezes isso acaba se tornando um inconveniente. Fazer com que 12 brasileiros retomem o foco depois de um exercício de conversação é sempre um desafio.</div>
