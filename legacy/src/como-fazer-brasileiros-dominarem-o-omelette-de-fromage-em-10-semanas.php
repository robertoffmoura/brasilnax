													<div class="paragraph" style="text-align:justify;">Joëlle Bonenfant possui mais de oito anos de experiência ensinando estudantes brasileiros da École Polytechnique durante o semestre preparatório. Esta professora querida por diferentes promoções brasileiras de <em>polytechniciens</em> nos contou quais as principais dificuldades dos nossos compatriotas com o francês e como eles podem maximizar o aprendizado da língua.</div>
