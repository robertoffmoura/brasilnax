													<div class="paragraph" style="text-align:justify;"><strong><font color="#2a2a2a">Carreira</font></strong><br><br><strong>Brasil na X: </strong>O que durante esse período na X você sentiu que mais ajudou a definir o seu rumo na carreira?<br><br><strong>EM: </strong>Eu acho que foi no estágio do terceiro ano, o estágio científico. Comecei a olhar com um amigo alemão essa área de cristais fotônicos me interessava bastante. Achamos então um professor da X (Claude Weisbuch) que fazia esse tipo de pesquisa. O estágio em si eu não gostei particularmente, mas foi o contato com o professor que me abriu portas para que eu pudesse ir para outros países depois.<br><br>Pra ser sincero, não teve nada que me ajudou diretamente no meu doutorado. Tudo que eu tive que aprender acabei tendo que aprender sozinho. O que me ajuda hoje de tudo que eu fiz na X foi: você perde o medo de coisas matemáticas. Então no meu doutorado, eu tenho papers (artigos) que eu publiquei puramente teóricos, mesmo eu não estando numa área de matemática. Eu pude desenvolver modelos matemáticos, pude fazer na prática o que eu queria. Eu conseguia explicar as coisas e acabei desenvolvendo modelos através dos quais eu ajudava todo mundo.<br><br>Eu acho que isso é como qualquer outra faculdade. O ensino direto não conta muito. Você vai aprender na hora o que você precisa aprender. Quando vocês forem fazer mestrado, doutorado, ou forem numa empresa, você vai aprender o que você precisa. Só que a gente não tem mais medo, você não acha que não tem a base suficiente. Você sabe que a gente pode aprender. Isso que eu achei que foi o melhor de toda essa experiência: não ter mais medo das coisas.<br><br><strong>Brasil na X: </strong>Então o que te ajudou a postular para fora foi o contato com o professor, não ter mais medo das coisas, não ter medo de ir a fundo…<br><br><strong>EM: </strong>E não desistir! O que eu vi foi que muitas pessoas que entraram láperderam a paixão pela ciência, porque foi uma experiência meio brutal pra todo mundo. Mas se você continua, começa a entender como melhorar no teu percurso.Você percebe que é só uma questão de tempo. Só tem um gap muito grande no começo e você só tem que completar ele.<br><br>Então depende da forma que você aceita isso. Tem que saber que vai ser brutal, mas tente aproveitar o máximo dessa experiência. Acho que isso é muito mais útil do que o programa em si. Se eu falar que teoria de distribuições que eu aprendi é útil pra mim agora, não. Mas o processo de ter aprendido aquilo é muito útil.</div>
													<div class="paragraph" style="text-align:justify;"><strong><font color="#2a2a2a">Academia</font></strong><br><strong>Brasil na X: </strong>Você tem conselhos para polytechniciens que querem seguir na sua área?<br><br><strong>EM: </strong>Eu acho que nos estágios científicos aproveitem para tentar coisas que vocês queiram fazer. O conselho mais importante é: não vá pelo nome da faculdade. Por exemplo, eu não escolhi o MIT, eu escolhi um grupo que por acaso estava no MIT, porque era o que eu queria fazer.<br>Eu tive vários amigos que foram pelo nome da faculdade e eles caíram na armadilha de perder totalmente o gosto pela coisa. Às vezes você vai para um grupo que você não gosta e com um professor que nunca está lá. Você tem um professor que tem 60 pessoas num grupo. Ele nem sabe que você é aluno dele. Essa é a pior coisa que você pode fazer.<br><br><strong>Escolhe primeiro o que te apaixona</strong>, pode ser que você não saiba. A gente nunca sabe. Use o estágio científico para descobrir essas coisas.<br><br>E essas conexões em geral te abrem portas. Eu fiz o PhD. na UCSB. O nome na época pode ser que atraia menos gente do que se eu falasse Harvard. Mas eu fui num grupo que tinha dois prêmios Nobel, que tinha a pesquisa em LED's que eu acreditava que podia fazer uma diferença gigante no mundo e por acaso eles estavam em UCSB.<br><br>Uma das coisas que eu mais gostei no meu PhD. foi que a gente sentava numa mesa, tinha um quadro negro e a gente ficava discutindo ideias. Eu adorei essa parte.<br><br><strong>Brasil na X: </strong>Alguma última mensagem?<br><br><strong>EM: </strong>Essa coisa de você definir o que você quer ser, montar um plano de carreira e ter um objetivo é super importante. Saber no que você é bom, o que você quer fazer e direcionar as ações que você realiza para isso. Não fazer coisas por fazer. Várias pessoas acabam seguindo esse caminho onde você está meio que à deriva, sabe? Sem uma direção, como um barco à deriva, você não sai do lugar.<br><br>Então defina uma direção, tenha persistência, saber que na Polytechnique vão ter coisas que não vão ser agradáveis, mas no final das contas vai valer a pena.<br><br>Se você acha que essa área de semicondutores ou de eletrônica de potência é interessante e se vocês querem testar, tem o meu laboratório, tem outros laboratórios. É só entrar em contato e falar: “olha, eu estou a fim de testar”, por que não?<br><br>Às vezes é nesse teste que você fala: “Achei o que eu gosto.”. Você descobre o potencial do que você está fazendo, que foi o que aconteceu comigo na área de led, que eu falei: “po perai, isso pode ser muito maior do que eu to pensando.”.</div>
