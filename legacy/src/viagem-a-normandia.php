													<div class="paragraph">A Polytechnique se importa muito com os alunos internacionais. Em vista disso, nesse ano, ao fim da formação preparatória e logo antes do tronco comum, foi realizada a primeira viagem dos alunos internacionais à Normandia. Ela teve o objetivo principal de ser uma primeira integração entre os extrangeiros e os francófonos e também de apresentar aos alunos internacionais alguns conceitos básicos de militarismo, pois, apesar de não sermos oficialmente membros das forças armadas francesa, ainda devemos participar de algumas cerimônias militares durante o tempo que ficaremos na Polytechnique.<br></div>
