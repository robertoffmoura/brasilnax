													<div class="paragraph" style="text-align:justify;">Antes da viagem, nós participamos de uma pesquisa sobre nossos interesses para formar uma dupla com algum aluno francês ou francófono de outro país (normalmente marroquino, como foi o meu caso). A formação das duplas tem o objetivo de ‘forçar’ um contato inicial, o que ajuda muito, pois a diferença do nível de francês é muito grande. Durante a viagem de ida para a Normandia, os integrantes de cada dupla se conhecem e, durante os quatro dias de viagem, todos os alunos ficaram em pequenos bangalôs próximos à praia, cada dupla se alojando no mesmo local. Além dos alunos, estavam presentes os nossos professores de esporte, que são militares que cuidam do ensino esportivo na X, o maestro da Polytechnique e o comandante militar da nossa turma.<br><br>Nós chegamos à tarde, já exaustos da viagem e não fizemos muita coisa. No segundo dia, às 6h da manhã, nós já tínhamos que estar de pé e fardados (a farda é um equivalente ao uniforme camuflado das forças armadas brasileiras) para tomar café e depois nós recebemos algumas instruções básicas, sobre como ficar em posições militares. Em seguida, começamos a aprender, com o maestro, a cantar algumas músicas, dentre elas a Marseillaise (hino nacional francês), que devem ser cantadas nas cerimônias militares. Após o almoço, nós fizemos um passeio pela praia, visitando vários pontos turísticos e também um museu sobre o Dia D e, por fim, tivemos um tempo livre para explorar a cidade.<br><br>No dia seguinte, tivemos o mesmo treinamento no início da manhã. Após isso, nós tivemos uma apresentação da X e de alunos do ano anterior, eles falaram sobre a vida acadêmica, os binets e sobre a X no geral. Durante a tarde, nós fomos divididos em alguns grupos e participamos de uma competição. Todos os grupos tinham que realizar várias atividades, como corrida de obstáculos, tiro ao alvo, lançamento de projéteis, revezamento na máquina de remo, revezamento de corrida e bicicleta, entre outros. No último dia, nós fizemos a repetição final do ensaio das músicas, tivemos outra apresentação sobre a X e, após o almoço, retornamos à X.<br><br>Eu sei que essa viagem pode parecer bem desgastante/intimidante para quem nunca teve contato com o militarismo, mas nossas atividades foram bem leves e os militares são bem pacientes com alunos extrangeiros. Para quem vem de escolas militares do Brasil, o treinamento militar que vocês viram foi bem pior. O principal objetivo da viagem era termos um primeiro contato com os nossos futuros colegas franceses, o que nos deu muito tempo para conhecê-los.<br></div>
