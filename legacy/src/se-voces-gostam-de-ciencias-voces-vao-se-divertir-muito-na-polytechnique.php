													<div class="paragraph" style="text-align:justify;"><strong><font color="#2a2a2a">Estudante no quadro </font></strong><br>Uma coisa que pode ser eliminatória são as <em>grandes bêtises</em>&nbsp;como chamamos em francês os erros muito bobos. Por exemplo, conceitos de álgebra linear elementar ou de análise elementar (cálculo). Os examinadores tentam contudo, medir quais erros o candidato fez porque estava nervoso. É um processo difícil, mas se após uma ajuda o estudante compreende que seu pensamento estava errado, não tem tanto problema. Romain acrescentou que os brasileiros são bons em calculo e em contas mais práticas (como multiplicação de matrizes), mas eles têm dificuldades com problemas mais abstratos e é nesta área que eles cometem os maiores erros.&nbsp;<br><br><strong><font color="#2a2a2a">Erros&nbsp;</font><br>​</strong>Os erros que Romain considera os mais frequentes entre os brasileiros são problemas com ordem de grandeza. Existem quantidades grandes, muito grandes e ainda maiores. É preciso entender bem a diferença entre elas. Por exemplo, na expansão de uma função em sua série de Taylor, os estudantes têm dificuldades em ver quais são os termos dominantes, em escrever o resto e desconsiderar os termos que são desprezíveis. Além disso, os brasileiros têm dificuldade de entender e fazer contar com aplicações lineares que não são associadas a uma matriz. Para essas dificuldades, ele sugeriu aos candidatos, <strong>aprender um pouco mais de matérias teóricas e até mesmo fazer cursos de verão no IMPA</strong>, mas é importante lembrar que quanto maior o nível do candidato, maior a dificuldade do exercício.<br><br>Esses erros não são erros bobos que, segundo nosso entrevistado, são erros que o estudante comete em alguma matéria que ele sabe, ou deveria saber. Erros bobos não são desclassificatórios se o candidato pensa rápido. Contudo, os examinadores tentam evitar pessoas com uma lacuna de conhecimento que o impediria de cursar as disciplinas na École Polytechnique.&nbsp;</div>
													<div class="paragraph" style="text-align:justify;"><strong><font color="#2a2a2a">Porque a X&nbsp;</font></strong><br>Os cursos de engenharia do Brasil têm dois anos mais gerais seguidos de cursos bem específicos. Romain disse que, baseado nas cartas de motivação que leu, os brasileiros que procuram a Poytechnique, buscam um conhecimento mais teórico. Se o aluno gosta de matérias teóricas, gosta de matemática e de física ou ciências em geral, ele vai se divertir na Polytechnique porque nesta escola estuda-se matemática sofisticada, física sofisticada e outras disciplinas com um nível bem avançado.</div>
