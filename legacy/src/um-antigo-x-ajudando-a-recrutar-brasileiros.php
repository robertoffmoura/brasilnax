													<div class="paragraph" style="text-align:justify;"><strong><font color="#2a2a2a">Percurso</font></strong><br>Depois de sair das escolas preparatórias francesas, Guillaume passou a fazer parte de promoção de 1992 da X. Depois realizou um mestrado e doutorado na <em>École Normale Supérieure </em>em Física Quântica até 1999. Realizou um pós-doutorado em Berlin e depois se tornou mestre de conferências na Universidade Paris VI. Hoje, é professor titular na <em>Université Montpellier</em>.<br><br>Decidiu se tornar examinador em 2006, quando ficou sabendo da oportunidade por um colega que na época era coordenador do concurso da parte de Física. Um dos motivos que o levaram a fazer esta escolha foi o distanciamento que estava sentido dos alunos da École Polytechnique.<br>Foi em 2010 que surgiu a proposta de ir ao Brasil como examinador. Sem saber nada sobre nosso país, Guillaume decidiu encarar o desafio e hoje já foi ao Brasil cinco vezes para ajudar a selecionar os alunos brasileiros.<br></div>
													<div class="paragraph" style="text-align:justify;"><strong><font color="#2a2a2a">O que eles esperam do candidato?</font></strong><br><span>Com uma lista de conteúdos de Física vasta, os examinadores olham em geral o dossiê do candidato para escolher o exercício mais adaptado ao seu percurso. Guillaume nos disse que o perfil em física dos brasileiros é bem diversificado. No Rio, a maioria dos alunos vem de Matemática Aplicada fazendo assim muito pouco de física, enquanto os alunos de engenharia de São Paulo possuem um nível em física similar ao dos franceses quando saem das escolas preparatórias.</span><br></div>
													<div class="paragraph" style="text-align:justify;"><span>Quando é o examinador, este X1992 prefere escolher exercícios simples, mas que lhe oferecem a oportunidade de tratar questões mais delicadas com os candidatos se necessário. Para ele, o mais importante é como o candidato reage. A prova possui uma dinâmica muito interativa, então o essencial não é ser perfeito enquanto estiver trabalhando na lousa (apesar de ser importante), mas sim saber se adaptar rapidamente e possuir reatividade ao receber indicações. “<strong>É o candidato mais adaptável que será escolhido</strong>”, nos contou Guillaume.</span></div>
													<div class="paragraph" style="text-align:justify;"><strong><font color="#2a2a2a">Aspectos matemáticos&nbsp;</font></strong><br><span>Os examinadores estão sim muito interessados na interpretação física do candidato, mas é imprescindível saber os aspectos matemáticos básicos. Se o candidato tem muitas lacunas de um ponto de vista formal, ele pode ter problemas durante seus estudos na X. Por isso, Guillaume verifica que o candidato tem habilidades matemáticas básicas, como resolver uma equação diferencial elementar ou desenvolver um cálculo sem muitos erros.</span><br><br><strong><font color="#2a2a2a">Impressões dos alunos brasileiros</font></strong><br><span>Em geral, Guillaume possui uma impressão excelente dos alunos brasileiros os caracterizando como “<strong>brilhantes</strong>”. Para ele, sempre é uma grande aposta ir ao estrangeiro e se adaptar a um sistema completamente diferente. É uma experiência extremamente enriquecedora, mas que exige muito esforço. Ele achou admirador de ver a vontade dos alunos brasileiros de ir buscas outras coisas no estrangeiro, estando muito abertos e com vontade de partir e descobrir coisas novas.</span></div>
