													<div class="paragraph" style="text-align:justify;"><span>Se você chegou a este site é porque, de algum modo, ouviu falar da École Polytechnique e se interessou pela ideia de vir estudar aqui. Provavelmente já conhece a sua reputação em matemática e ciências, suas tradições e seu belo uniforme. O que você talvez não conheça é a riqueza e a diversidade da vida associativa no seu campus. Além das tradições e da vida dentro das seções esportivas, são essas associações (chamadas binets) que ajudam a criar o sentimento de </span><em>promotion</em><span> que une a cada ano todos os 500 polytechniciens das mais diversas origens.</span><br><span> </span><br><span>A vida associativa na X  começou a se desenvolver a um ritmo impressionante depois que a escola se mudou do quartier Latin em Paris, com toda a sua vida animada, para o plateau de Saclay, basicamente um grande campo de batata na época. Muitos anos se passaram, mas as coisas não mudaram muito. O relativo isolamento do campus (40 minutos de trem até Paris), a boa saúde financeira da escola e, principalmente, a motivação e a criatividade de seus alunos são os principais motores dessa vida associativa. </span></div>
													<div class="paragraph" style="text-align:justify;"><span>O número de associações se conta em centenas e está sempre em mutação, dado que a cada ano novos binets são criados. Como exemplo, você vai encontrar binets ligados a esportes (ski, paraquedismo, vela, karatê,...), culinária (queijos e vinhos, cervejas, chocolate, kebab,...), artes (heavy metal, coral, jazz, teatro, dança de salão, mágica,...), culturas do mundo (latina, russa, germânica, chinesa, árabe,...), confissões religiosas (cristã, judaica), científicos (robótica, astronomia, programação,...), empresariais (consultoria, empresa junior, start-up,...), ações humanitárias (coleta de roupas, assistência a crianças hospitalizadas,...) e muito mais.</span><br><span> </span><br><span>No centro de tudo isso está a </span><strong>Kès</strong><span>, o </span><em>bureau des élèves</em><span> da X. Composta de 16 estudantes, cada um com uma responsabilidade diferente, ela tem como funções: financiar e auxiliar o funcionamento de todos os binets, trabalhando em parceria com os presidentes de cada um ao longo do ano; representar os estudantes perante a administração; comandar diretamente diversas atividades, como a integração dos calouros, o jornal da escola e as principais festas; estar presente em todo grande evento organizado no campus. O dinheiro que alimenta a </span><strong>Kès</strong><span> (e todos os binets indiretamente) provém de principalmente de três fontes: alunos, escola e empresas.</span></div>
													<div class="paragraph" style="text-align:justify;"><span>A escolha dos estudantes que compõem a </span><strong>Kès</strong><span> (os </span><em>kessiers</em><span>) é feita através de uma votação em que participam todos os alunos que moram no campus. Após uma semana de campanha inesquecível, que conta com uma série de eventos e muita comida grátis, a chapa com mais voto é eleita. Em geral as chapas são formadas bem cedo, durante o tronco comum. Dentre os 16 membros existe um posto que interessa particularmente os estudantes internacionais, o de </span><em>kessier inter</em><span>, responsável pela integração e representação de estudantes internacionais. A lenda diz que o brasileiro ou a brasileira mais atraente de cada promoção acaba virando aspirante a<em> kessier</em> <em>inter</em>.</span></div>
