													<div class="paragraph" style="text-align:justify;">De modo geral, os binets que exigem um maior investimento possuem uma equipe fixa durante um ano. Os novos membros são escolhidos durante o segundo ano pelos membros antigos após um período chamado de <em>pré-passation</em>. Durante esse período os chamados aspirantes participam nas atividades dos binets e podem ser avaliados pela sua motivação e capacidade de assumir as responsabilidades do binet em questão. Ao fim dessa seleção, os novos membros substituem os antigos num processo chamado de <em>passation</em>, que é em geral seguido de uma festa aberta ou não a toda promoção.<br> <br>Dentre os binets que possuem uma <em>pré-passation</em> mais disputada e que, consequentemente, exigem um investimento e possuem um impacto maior na vida da promoção, podemos citar: <strong>JTX</strong> - escreve, produz e edita filmes dos mais diversos temas e formatos, indo desde o simples registro de um evento a uma produção digna de um Urso de Ouro. <strong>Bôbar</strong> - equipe que mantém o bar da escola e que se aproveita disso pra bater na sua porta às 3 da manhã se você não pagar suas dívidas. <strong>STYX</strong> - organiza e anima, sozinho ou em colaboração com outros binets, (quase) todas as festas que ocorrem no campus. <strong>Khômiss</strong> - binet responsável por transmitir as tradições da X, das maneiras mais diversas e inesperadas. <strong>X-Projets</strong> - empresa júnior da X e uma ótima oportunidade de conseguir aquele contato com o CEO de uma grande empresa.</div>
